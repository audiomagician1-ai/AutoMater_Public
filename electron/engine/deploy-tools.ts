export interface NginxSiteConfig {
  serverName: string;
  listenPort?: number;
  /** 后端服务地址 */
  upstream: string;
  /** 静态文件根目录 */
  staticRoot?: string;
  /** 启用 SSL (需提供 cert/key 路径) */
  ssl?: {
    certPath: string;
    keyPath: string;
  };
  /** SPA 模式 — 所有路由 fallback 到 index.html */
  spaMode?: boolean;
  /** 额外 location 块 */
  extraLocations?: Array<{
    path: string;
    proxyPass?: string;
    root?: string;
    tryFiles?: string;
  }>;
}

/**
 * 生成 Nginx 站点配置
 */
export function generateNginxConfig(config: NginxSiteConfig): string {
  const lines: string[] = [];
  const port = config.listenPort || (config.ssl ? 443 : 80);

  lines.push(`# Nginx config — Auto-generated by AutoMater`);
  lines.push(`# Server: ${config.serverName}`);
  lines.push('');
  lines.push(`upstream backend_${config.serverName.replace(/\./g, '_')} {`);
  lines.push(`    server ${config.upstream};`);
  lines.push('}');
  lines.push('');
  lines.push('server {');
  lines.push(`    listen ${port}${config.ssl ? ' ssl http2' : ''};`);
  lines.push(`    server_name ${config.serverName};`);
  lines.push('');

  if (config.ssl) {
    lines.push(`    ssl_certificate     ${config.ssl.certPath};`);
    lines.push(`    ssl_certificate_key ${config.ssl.keyPath};`);
    lines.push(`    ssl_protocols       TLSv1.2 TLSv1.3;`);
    lines.push(`    ssl_ciphers         HIGH:!aNULL:!MD5;`);
    lines.push('');
  }

  // Gzip
  lines.push('    gzip on;');
  lines.push('    gzip_types text/plain text/css application/json application/javascript text/xml;');
  lines.push('    gzip_min_length 1024;');
  lines.push('');

  // 静态文件
  if (config.staticRoot) {
    lines.push(`    root ${config.staticRoot};`);
    lines.push('');
    lines.push('    location /assets/ {');
    lines.push('        expires 30d;');
    lines.push('        add_header Cache-Control "public, immutable";');
    lines.push('    }');
    lines.push('');
  }

  // API 反向代理
  lines.push('    location /api/ {');
  lines.push(`        proxy_pass http://backend_${config.serverName.replace(/\./g, '_')};`);
  lines.push('        proxy_http_version 1.1;');
  lines.push('        proxy_set_header Upgrade $http_upgrade;');
  lines.push('        proxy_set_header Connection "upgrade";');
  lines.push('        proxy_set_header Host $host;');
  lines.push('        proxy_set_header X-Real-IP $remote_addr;');
  lines.push('        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;');
  lines.push('        proxy_set_header X-Forwarded-Proto $scheme;');
  lines.push('        proxy_read_timeout 300s;');
  lines.push('    }');
  lines.push('');

  // 额外 location 块
  if (config.extraLocations) {
    for (const loc of config.extraLocations) {
      lines.push(`    location ${loc.path} {`);
      if (loc.proxyPass) lines.push(`        proxy_pass ${loc.proxyPass};`);
      if (loc.root) lines.push(`        root ${loc.root};`);
      if (loc.tryFiles) lines.push(`        try_files ${loc.tryFiles};`);
      lines.push('    }');
      lines.push('');
    }
  }

  // SPA fallback
  if (config.spaMode) {
    lines.push('    location / {');
    lines.push('        try_files $uri $uri/ /index.html;');
    lines.push('    }');
  } else if (!config.staticRoot) {
    // 纯反向代理模式
    lines.push('    location / {');
    lines.push(`        proxy_pass http://backend_${config.serverName.replace(/\./g, '_')};`);
    lines.push('    }');
  }

  lines.push('}');
  return lines.join('\n');
}

/**
 * 写入 nginx 配置到指定路径
 */
export async function writeNginxConfig(
  config: NginxSiteConfig,
  outputDir: string,
): Promise<{ success: boolean; filePath: string; error?: string }> {
  try {
    fs.mkdirSync(outputDir, { recursive: true });
    const filePath = path.join(outputDir, `${config.serverName}.conf`);
    const content = generateNginxConfig(config);
    fs.writeFileSync(filePath, content, 'utf-8');
    log.info(`Nginx config written: ${filePath}`);
    return { success: true, filePath };
  } catch (err: unknown) {
    return { success: false, filePath: '', error: err instanceof Error ? err.message : String(err) };
  }
}

