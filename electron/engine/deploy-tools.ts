/**
 * 生成 docker-compose.yml 内容
 */
export function generateComposeYaml(config: ComposeConfig): string {
  const lines: string[] = [];

  // docker compose 规范版本不再需要，用默认最新 compose spec
  lines.push(`# Auto-generated by AutoMater Deploy Tools`);
  lines.push(`# Project: ${config.projectName}`);
  lines.push(`# Generated: ${new Date().toISOString()}`);
  lines.push('');
  lines.push('services:');

  for (const svc of config.services) {
    lines.push(`  ${svc.name}:`);

    if (svc.image) {
      lines.push(`    image: ${svc.image}`);
    }
    if (svc.build) {
      lines.push(`    build:`);
      lines.push(`      context: .`);
      lines.push(`      dockerfile: ${svc.build}`);
    }
    if (svc.command) {
      lines.push(`    command: ${svc.command}`);
    }
    if (svc.ports.length > 0) {
      lines.push(`    ports:`);
      for (const p of svc.ports) {
        lines.push(`      - "${p}"`);
      }
    }
    if (svc.env && Object.keys(svc.env).length > 0) {
      lines.push(`    environment:`);
      for (const [k, v] of Object.entries(svc.env)) {
        lines.push(`      ${k}: "${v}"`);
      }
    }
    if (svc.volumes && svc.volumes.length > 0) {
      lines.push(`    volumes:`);
      for (const v of svc.volumes) {
        lines.push(`      - ${v}`);
      }
    }
    if (svc.dependsOn && svc.dependsOn.length > 0) {
      lines.push(`    depends_on:`);
      for (const d of svc.dependsOn) {
        lines.push(`      - ${d}`);
      }
    }
    if (svc.healthCheck) {
      lines.push(`    healthcheck:`);
      lines.push(`      test: ${svc.healthCheck}`);
      lines.push(`      interval: 10s`);
      lines.push(`      timeout: 5s`);
      lines.push(`      retries: 3`);
    }
    if (svc.restart) {
      lines.push(`    restart: ${svc.restart}`);
    }
    if (svc.memoryLimit) {
      lines.push(`    deploy:`);
      lines.push(`      resources:`);
      lines.push(`        limits:`);
      lines.push(`          memory: ${svc.memoryLimit}`);
    }
    lines.push('');
  }

  if (config.networkName) {
    lines.push('networks:');
    lines.push(`  ${config.networkName}:`);
    lines.push(`    driver: bridge`);
  }

  return lines.join('\n');
}

/**
 * 将 compose 配置写入文件并执行 docker compose up
 */
export async function deployWithCompose(
  config: ComposeConfig,
  workspacePath: string,
  options?: { detach?: boolean; buildFirst?: boolean; timeout?: number },
): Promise<DeployResult> {
  const generatedFiles: string[] = [];
  const urls: string[] = [];

  try {
    // 1. 生成 docker-compose.yml
    const yaml = generateComposeYaml(config);
    const composePath = path.join(workspacePath, 'docker-compose.yml');
    fs.writeFileSync(composePath, yaml, 'utf-8');
    generatedFiles.push(composePath);
    log.info(`Generated docker-compose.yml at ${composePath}`);

    // 2. 执行 docker compose up
    const detachFlag = options?.detach !== false ? '-d' : '';
    const buildFlag = options?.buildFirst ? '--build' : '';
    const cmd = `docker compose -f "${composePath}" up ${detachFlag} ${buildFlag}`.trim();

    log.info(`Executing: ${cmd}`);
    const timeout = options?.timeout || 120_000;
    const { stdout, stderr } = await execAsync(cmd, { cwd: workspacePath, timeout });

    // 3. 收集 URLs
    for (const svc of config.services) {
      for (const portMap of svc.ports) {
        const hostPort = portMap.split(':')[0];
        urls.push(`http://localhost:${hostPort}`);
      }
    }

    return {
      success: true,
      output: `部署成功 ✅\n\n${stdout}\n${stderr ? `⚠️ ${stderr}` : ''}\n\n可访问 URL:\n${urls.map(u => `  → ${u}`).join('\n')}`,
      generatedFiles,
      urls,
    };
  } catch (err: unknown) {
    const msg = err instanceof Error ? err.message : String(err);
    log.error(`Compose deploy failed: ${msg}`);
    return { success: false, output: '', error: msg, generatedFiles, urls };
  }
}

/**
 * 停止并清理 compose 服务
 */
export async function composeDown(
  workspacePath: string,
): Promise<{ success: boolean; output: string; error?: string }> {
  try {
    const composePath = path.join(workspacePath, 'docker-compose.yml');
    if (!fs.existsSync(composePath)) {
      return { success: false, output: '', error: 'docker-compose.yml 不存在' };
    }
    const { stdout, stderr } = await execAsync(`docker compose -f "${composePath}" down -v`, {
      cwd: workspacePath,
      timeout: 60_000,
    });
    return { success: true, output: `服务已停止并清理\n${stdout}\n${stderr}` };
  } catch (err: unknown) {
    return { success: false, output: '', error: err instanceof Error ? err.message : String(err) };
  }
}

