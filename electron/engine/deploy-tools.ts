export interface DockerfileConfig {
  /** 基础镜像 */
  baseImage: string;
  /** 工作目录 */
  workDir?: string;
  /** 安装依赖命令 */
  installCmd?: string;
  /** 构建命令 */
  buildCmd?: string;
  /** 暴露端口 */
  exposePorts?: number[];
  /** 启动命令 */
  startCmd: string;
  /** 复制文件 */
  copyFiles?: Array<{ src: string; dest: string }>;
  /** 多阶段构建: builder 阶段 */
  multiStage?: {
    builderImage: string;
    builderSteps: string[];
    copyFromBuilder: Array<{ src: string; dest: string }>;
  };
}

/**
 * 生成 Dockerfile
 */
export function generateDockerfile(config: DockerfileConfig): string {
  const lines: string[] = [];

  lines.push('# Dockerfile — Auto-generated by AutoMater');
  lines.push(`# Generated: ${new Date().toISOString()}`);
  lines.push('');

  if (config.multiStage) {
    // Multi-stage build
    lines.push(`FROM ${config.multiStage.builderImage} AS builder`);
    lines.push(`WORKDIR ${config.workDir || '/app'}`);
    for (const step of config.multiStage.builderSteps) {
      lines.push(step);
    }
    lines.push('');
    lines.push(`FROM ${config.baseImage}`);
    lines.push(`WORKDIR ${config.workDir || '/app'}`);
    for (const copy of config.multiStage.copyFromBuilder) {
      lines.push(`COPY --from=builder ${copy.src} ${copy.dest}`);
    }
  } else {
    lines.push(`FROM ${config.baseImage}`);
    lines.push(`WORKDIR ${config.workDir || '/app'}`);
  }

  // Copy files
  if (config.copyFiles) {
    for (const f of config.copyFiles) {
      lines.push(`COPY ${f.src} ${f.dest}`);
    }
  } else {
    lines.push('COPY . .');
  }

  // Install
  if (config.installCmd) {
    lines.push(`RUN ${config.installCmd}`);
  }

  // Build
  if (config.buildCmd) {
    lines.push(`RUN ${config.buildCmd}`);
  }

  // Expose
  if (config.exposePorts) {
    for (const p of config.exposePorts) {
      lines.push(`EXPOSE ${p}`);
    }
  }

  lines.push('');
  lines.push(`CMD ${config.startCmd}`);

  return lines.join('\n');
}

/**
 * 写入 Dockerfile
 */
export async function writeDockerfile(
  config: DockerfileConfig,
  outputPath: string,
): Promise<{ success: boolean; filePath: string; error?: string }> {
  try {
    const dir = path.dirname(outputPath);
    fs.mkdirSync(dir, { recursive: true });
    const content = generateDockerfile(config);
    fs.writeFileSync(outputPath, content, 'utf-8');
    log.info(`Dockerfile written: ${outputPath}`);
    return { success: true, filePath: outputPath };
  } catch (err: unknown) {
    return { success: false, filePath: '', error: err instanceof Error ? err.message : String(err) };
  }
}
