/**
 * 生成 PM2 ecosystem 配置文件
 */
export function generatePM2Ecosystem(apps: PM2AppConfig[]): string {
  const ecosystem = {
    apps: apps.map(app => ({
      name: app.name,
      script: app.script,
      cwd: app.cwd || '.',
      args: app.args || '',
      instances: app.instances || 1,
      env: app.env || {},
      max_memory_restart: app.maxMemoryRestart || '500M',
      log_file: app.logFile || `./logs/${app.name}.log`,
      watch: app.watch || false,
      autorestart: true,
      merge_logs: true,
    })),
  };

  return `// PM2 Ecosystem — Auto-generated by AutoMater
module.exports = ${JSON.stringify(ecosystem, null, 2)};
`;
}

/**
 * 启动 PM2 进程
 */
export async function pm2Start(apps: PM2AppConfig[], workspacePath: string): Promise<DeployResult> {
  const generatedFiles: string[] = [];

  try {
    // 生成 ecosystem.config.js
    const content = generatePM2Ecosystem(apps);
    const configPath = path.join(workspacePath, 'ecosystem.config.js');
    fs.writeFileSync(configPath, content, 'utf-8');
    generatedFiles.push(configPath);

    // 确保 logs 目录存在
    const logsDir = path.join(workspacePath, 'logs');
    fs.mkdirSync(logsDir, { recursive: true });

    // 启动
    const { stdout, stderr } = await execAsync(`npx pm2 start ecosystem.config.js`, {
      cwd: workspacePath,
      timeout: 30_000,
    });

    return {
      success: true,
      output: `PM2 启动成功 ✅\n${stdout}\n${stderr || ''}`,
      generatedFiles,
      urls: [],
    };
  } catch (err: unknown) {
    return {
      success: false,
      output: '',
      error: err instanceof Error ? err.message : String(err),
      generatedFiles,
      urls: [],
    };
  }
}

/**
 * PM2 状态查询
 */
export async function pm2Status(): Promise<{ success: boolean; output: string }> {
  try {
    const { stdout } = await execAsync('npx pm2 jlist', { timeout: 10_000 });
    const processes = JSON.parse(stdout) as Array<{
      name: string;
      pm2_env: { status: string; pm_uptime: number; restart_time: number };
      monit: { cpu: number; memory: number };
    }>;

    if (processes.length === 0) return { success: true, output: '无 PM2 进程运行' };

    const lines = processes.map(p => {
      const uptime = Math.round((Date.now() - p.pm2_env.pm_uptime) / 1000);
      const mem = Math.round(p.monit.memory / 1024 / 1024);
      return `${p.name}: ${p.pm2_env.status} | uptime=${uptime}s | cpu=${p.monit.cpu}% | mem=${mem}MB | restarts=${p.pm2_env.restart_time}`;
    });

    return { success: true, output: lines.join('\n') };
  } catch (err: unknown) {
    return { success: false, output: err instanceof Error ? err.message : String(err) };
  }
}

