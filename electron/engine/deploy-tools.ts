/**
 * 检测端口是否可用
 */
export async function findAvailablePort(startPort: number = 3000, endPort: number = 9999): Promise<number> {
  // 使用 net 模块测试端口
  const net = await import('net');

  for (let port = startPort; port <= endPort; port++) {
    const available = await new Promise<boolean>(resolve => {
      const server = net.createServer();
      server.listen(port, '127.0.0.1');
      server.on('listening', () => {
        server.close();
        resolve(true);
      });
      server.on('error', () => resolve(false));
    });
    if (available) return port;
  }
  throw new Error(`无可用端口 (${startPort}-${endPort})`);
}

/**
 * 健康检查 — 轮询 URL 直到可达或超时
 */
export async function healthCheck(
  urls: Array<{ name: string; url: string; expectedStatus?: number }>,
  options?: { timeout?: number; interval?: number },
): Promise<HealthCheckResult> {
  const timeout = options?.timeout || 60_000;
  const interval = options?.interval || 2_000;
  const deadline = Date.now() + timeout;

  const results: HealthCheckResult['services'] = [];

  for (const target of urls) {
    const expected = target.expectedStatus || 200;
    let healthy = false;
    let responseTime = 0;
    let lastError = '';

    while (Date.now() < deadline) {
      const start = Date.now();
      try {
        const res = await fetch(target.url, {
          signal: AbortSignal.timeout(5_000),
        });
        responseTime = Date.now() - start;

        if (res.status === expected) {
          healthy = true;
          break;
        }
        lastError = `HTTP ${res.status} (expected ${expected})`;
      } catch (err: unknown) {
        lastError = err instanceof Error ? err.message : String(err);
        responseTime = Date.now() - start;
      }

      await new Promise(r => setTimeout(r, interval));
    }

    results.push({
      name: target.name,
      healthy,
      url: target.url,
      responseTime,
      error: healthy ? undefined : lastError,
    });
  }

  return {
    success: results.every(r => r.healthy),
    services: results,
  };
}

